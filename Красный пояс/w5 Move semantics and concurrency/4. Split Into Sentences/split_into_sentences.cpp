#include <vector>

using namespace std;

// Объявляем Sentence<Token> для произвольного типа Token
// синонимом vector<Token>.
// Благодаря этому в качестве возвращаемого значения
// функции можно указать не малопонятный вектор векторов,
// а вектор предложений — vector<Sentence<Token>>.
template <typename Token>
using Sentence = vector<Token>;

// Класс Token имеет метод bool IsEndSentencePunctuation() const
template <typename Token>
std::vector<Sentence<Token>> SplitIntoSentences(std::vector<Token> tokens) {
  std::vector<Sentence<Token>> result;
  Sentence<Token> temp_sentence;
  for (auto i_token = tokens.begin(); i_token != tokens.end();) {
    if (i_token->IsEndSentencePunctuation()) {
      while (i_token->IsEndSentencePunctuation() && i_token != tokens.end()) {
        temp_sentence.push_back(std::move(*i_token));
        ++i_token;
      }
      result.push_back(std::move(temp_sentence));
    }
    else {
      temp_sentence.push_back(std::move(*i_token));
      ++i_token;
    }
  }
  if (!temp_sentence.empty()) {
    result.push_back(std::move(temp_sentence));
  }
  return result;
}

